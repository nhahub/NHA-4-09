## Overview

While anxiety disorders vary based on symptoms and triggers, they can all interfere with daily activities. Understanding the different types can help people recognize their symptoms. Anxiety disorders can feel scary and disruptive, but help is available.

Here’s a closer look at four common types of anxiety disorders.

---

## Generalized anxiety disorder

People with generalized anxiety disorder (GAD) feel frequent extreme anxiety or worry for months, if not years. They may feel restless, on edge, or easily tired. They may also experience concentration problems, irritability, muscle tension, or sleep issues.

Treatment options include cognitive behavioral therapy, relaxation techniques, and sometimes medication.

---

## Panic disorder

People with panic disorder experience frequent and unexpected panic attacks. These are sudden, intense feelings of fear or terror when no immediate danger is present.

**Symptoms may include:**
- Fast heartbeat  
- Chest or stomach pain  
- Trouble breathing  
- Dizziness or weakness  
- Sweating or chills  
- Numb hands  

During a panic attack, a person may feel detached from reality or fear losing control. Some may think they are having a heart attack or that something terrible is happening.

Treatment includes cognitive behavioral therapy and sometimes medication.

---

## Phobia-related disorders

A phobia is an intense fear of specific objects or situations that is out of proportion to actual danger.

Examples:
- Agoraphobia (fear of public places)
- Claustrophobia (fear of closed spaces)
- Fear of flying, heights, animals, or needles

People may avoid triggers completely and experience immediate anxiety when exposed.

Treatment includes exposure therapy and sometimes medication.

---

## Social anxiety disorder

People with social anxiety disorder experience intense fear of social situations. They may worry about embarrassment or being judged.

This can affect work, school, and social life.

Treatment includes therapy, medication, and support groups.
