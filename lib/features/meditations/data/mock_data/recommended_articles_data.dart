## Overview

Wondering how you can help a friend, family member, or co-worker who is overwhelmed by anxiety?

We spoke to David Sommers, Ph.D., of NIH's National Institute of Mental Health for some suggestions on how to approach and support a person who might need help.

Remember that while some anxiety is normal, anxiety that interferes with a person’s daily life for long periods of time may be serious.

Here are some things to keep in mind:

---

## Consider your approach
How you reach out to a friend or co-worker will differ from how you approach a conversation with a spouse, parent, or child. Consider your relationship and which approaches may be more effective. Your aim is for your concern to be well received.

---

## Ask questions
Start by asking how they’re feeling. A common answer is “I’m fine,” so be prepared to follow up. You might ask whether they’re sure, because you’ve noticed changes like withdrawal, sleeping a lot, or agitation.

---

## Listen
Sometimes all your loved one needs is for someone to listen. Avoid interrupting or acting like a medical professional. Pay attention to body language, listen carefully, and be encouraging.

---

## Normalize their experience
Everyone has experienced anxiety. Share that you’ve had similar feelings before and understand how difficult it can be. Recognizing feelings is important, but avoid minimizing them.

---

## Provide resources
Prepare a list of helpful resources such as physicians, therapists, or community clinics that they can contact if they are ready.

---

## Follow up
Check in every day or two after your conversation. Ask how they’re doing, and encourage them to seek professional help if they haven’t yet.
